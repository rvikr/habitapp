-- New accounts start on the free tier. Google Play owns trial eligibility and
-- conversion for subscriptions. Existing profile trial timestamps are left
-- untouched so previously granted trials remain active until their expiry.

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public, pg_temp
as $$
begin
  insert into public.profiles (user_id)
  values (new.id)
  on conflict (user_id) do nothing;
  return new;
end;
$$;

revoke execute on function public.handle_new_user() from public;
revoke execute on function public.handle_new_user() from anon;
revoke execute on function public.handle_new_user() from authenticated;
